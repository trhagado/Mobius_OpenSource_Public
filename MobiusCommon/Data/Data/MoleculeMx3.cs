		/// <returns></returns>

		public object BuildBitSetFingerprint(
			FingerprintType fpType,
			int fpSubtype = -1,
			int fpLen = -1)
		{
			string molfile = GetMolfileString();

			object fp = MolLibStatic.I.BuildBitSetFingerprint(molfile, fpType, fpSubtype, fpLen);
			return fp;
		}

		/// <summary>
		/// Calculate Tanimoto similarity between two bitset fingerprints
		/// </summary>
		/// <param name="queryFingerprint"></param>
		/// <param name="targetFingerprint"></param>
		/// <returns></returns>

		public static double CalculateBitSetFingerprintSimilarity(
			object queryFingerprint,
			object targetFingerprint)
		{
			double score = MolLibStatic.I.CalculateBitSetFingerprintSimilarity(queryFingerprint, targetFingerprint);
			return score;
		}

